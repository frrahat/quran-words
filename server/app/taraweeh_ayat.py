@dataclass
class AyahInfo:
    sura: int
    ayah: int


def get_start_end_ayah_by_day(day: int):
    day_verse = DAY_VERSES[(day - 1) % 27]
    start_sura_ayah, _ = day_verse[0].split(" & ")[0].split(" - ")
    _, end_sura_ayah = day_verse[-1].split(" & ")[-1].split(" - ")

    start_sura, start_ayah = start_sura_ayah.split(":")
    end_sura, end_ayah = end_sura_ayah.split(":")

    return AyahInfo(int(start_sura), int(start_ayah)), AyahInfo(
        int(end_sura), int(end_ayah)
    )
